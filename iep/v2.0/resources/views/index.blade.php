              В 1960 году техникум передается в ведение Мосгорсовнархоза и «в связи с изменением профиля подготавливаемых специалистов» переименовывается в Московский радиоаппаратостроительный техникум (постановление Мосгорсовнархоза от 03 июня 1960 года № 59). Директором техникума назначается Кулагин Николай Павлович, который проявил себя не только талантливым администратором, но и интересным педагогом. Студенты старших курсов участвуют во всесоюзном конкурсе творческих работ молодежи, наши экспонаты представляются на ВДНХ, идет активное обновление оборудования учебных лабораторий. В радиомонтажных мастерских, которыми заведует Яковлев Г.Н., длительное время учащиеся занимались сборкой и наладкой отдельных блоков телевизионных приемников и других устройств радиоэлектронных систем. Свыше 50 процентов дипломных работ выпускников ежегодно внедрялись в производство предприятий радиоэлектронной промышленности. Наши учащиеся не раз заслуживали благодарность руководства заводов «Темп», «Рубин», «Янтарь», на которых проходили производственную практику, за высокий уровень знаний и ответственное отношение к порученному заданию. В 1965 году в соответствии с постановлением Совета Министров СССР от 30 декабря 1965 года № 1145 техникум передается Министерству радиопромышленности СССР. 
