              В 60-80-ые годы подлинно массовым патриотическим движением молодежи высших и средних специальных учебных заведений стали Студенческие строительные отряды (ССО). Опыт деятельности студенческих отрядов показал, что они играли важную роль в идейно-политическом и трудовом воспитании молодежи, способствовали повышению общественной активности и творческой инициативы. Первые ССО были организованы в 1959, и участвовать в них могли только студенты высших учебных заведений. Но техникумы боролись за право участия в строительных отрядах. В 1969 году техникум был включен во Всесоюзный студенческий строительный отряд. ССО – это не только посильное участие студентов в приращении материальных ресурсов страны, но ещё и одна из эффективных форм общего развития и воспитания молодёжи через общежитие. Последнее приобретает особую значимость для студенчества техникумов, когда совсем ещё молодые люди, освободившись из-под опеки родителей, попадают в среду своих сверстников, в среду ещё не испытанной взаимозависимости, среду взаимовлияния и требовательности под присмотром старшего товарища – командира отряда. В техникуме ежегодно по возвращении ССО с объектов подводились итоги, анализировались плюсы и минусы с целью учёта на будущее.
              В СССР с середины 60-х годов начинается бурное развитие АСУ. Закладываются основы государственной и межгосударственной системы обработки данных. Разрабатываются универсальные ЭВМ третьего поколения ЕС, совместимые как между собой, так и с зарубежными ЭВМ третьего поколения. Начинается разработка специальных программ, предназначенных для экономических расчетов, рассматриваются различные организационные проекты, формируются необходимые предпосылки для расширения и углубления автоматизации. Существенным фактором, тормозившим процесс автоматизации, была высокая стоимость техники и сложность ее эксплуатации, а также недостаток специалистов по эксплуатации вычислительной техники, их численность значительно отставала от потребностей народного хозяйства.
